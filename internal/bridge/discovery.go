package bridge

import (
	"context"
	"log"
	"time"

	"github.com/anthropics/claude-code-gateway/internal/runtime"
	"github.com/anthropics/claude-code-gateway/internal/session"
)

// startDiscoveryLoop runs an initial scan and then re-scans every
// rescanInterval until ctx is canceled. Disabled when discoverer is nil.
func (b *Bridge) startDiscoveryLoop(ctx context.Context) {
	if b.discoverer == nil {
		return
	}
	go func() {
		// initial scan
		b.runDiscovery(ctx)
		if b.rescanInterval <= 0 {
			return
		}
		t := time.NewTicker(b.rescanInterval)
		defer t.Stop()
		for {
			select {
			case <-ctx.Done():
				return
			case <-t.C:
				b.runDiscovery(ctx)
			}
		}
	}()
}

// runDiscovery scans the discoverer and imports any newly-seen sessions as
// idle, Origin=external entries in the session manager.
func (b *Bridge) runDiscovery(ctx context.Context) {
	if b.discoverer == nil {
		return
	}
	t0 := time.Now()
	records, err := b.discoverer.Scan(ctx, runtime.ScanOpts{WindowDays: b.discoveryWindowDays})
	if err != nil {
		log.Printf("[discovery] scan failed: %v", err)
		return
	}

	newly := 0
	enqueued := 0
	for _, r := range records {
		// Already-imported branch: don't re-import (would be a no-op
		// update), but still ensure the worker has a chance to summarize
		// it. Without this, sessions that we couldn't summarize on the
		// previous run (admin error, gateway killed mid-job, …) stay
		// stuck forever — discovery sees them, sees they exist, and
		// continues without ever re-enqueueing.
		if existing, exists := b.mgr.GetByCLISessionID(r.RuntimeID); exists {
			if existing.Info().Origin == session.OriginAdmin {
				continue
			}
			if b.worker == nil {
				continue
			}
			// Skip if a fresh v(current) summary already exists —
			// regenerating costs an admin round and produces nothing new.
			if aug, ok := b.mgr.ExternalSummary(r.RuntimeID); ok &&
				aug.PromptVersion >= SummaryPromptVersion && aug.Summary != "" {
				continue
			}
			// Skip if user gave it a custom title.
			if existing.Info().CustomTitle != "" {
				continue
			}
			b.worker.Enqueue(summaryJob{SessionID: existing.ID, CLISessionID: r.RuntimeID, SourceRef: r.SourceRef})
			enqueued++
			continue
		}

		// Admin-internal sessions still get imported into the manager (so
		// the system stays origin-agnostic — manager is the single truth)
		// but with Origin=OriginAdmin. All consumers (ListDiscoverableByOwner,
		// ResolveResumable, worker) filter by Origin and never expose admin
		// sessions to the user.
		if r.IsAdminInternal {
			_, _ = b.mgr.ImportIdleSession(session.ImportOpts{
				CLISessionID: r.RuntimeID,
				WorkingDir:   r.WorkingDir,
				Origin:       session.OriginAdmin,
				LastActivity: r.LastActivity,
			})
			continue
		}

		// Prefer persisted summary (set by a previous worker run) over the
		// lastPrompt placeholder so restarts don't re-enqueue work — UNLESS
		// the persisted summary was generated by an older prompt version,
		// in which case we want to regenerate.
		//
		// Empty Summary at the current PromptVersion is a deliberate "skip"
		// marker (worker classified the session as meta/empty). Treat it as
		// fresh so we don't re-enqueue every restart.
		summary := r.InitialSummary
		customTitle := r.CustomTitle
		hasFreshSummary := false
		if aug, ok := b.mgr.ExternalSummary(r.RuntimeID); ok {
			if aug.Summary != "" {
				summary = aug.Summary
			}
			if aug.PromptVersion >= SummaryPromptVersion {
				hasFreshSummary = true
			}
			if aug.CustomTitle != "" {
				customTitle = aug.CustomTitle
			}
		}

		sessionID, err := b.mgr.ImportIdleSession(session.ImportOpts{
			CLISessionID:      r.RuntimeID,
			WorkingDir:        r.WorkingDir,
			Summary:           summary,
			CustomTitle:       customTitle,
			LatestUserMessage: r.InitialSummary,
			Origin:            session.OriginExternal,
			LastActivity:      r.LastActivity,
			MessageCount:      r.MessageCount,
		})
		if err != nil {
			log.Printf("[discovery] import %s failed: %v", r.RuntimeID, err)
			continue
		}
		newly++

		// Only enqueue for AI summary when there's no custom title (user
		// already named it) and no fresh-enough persisted summary yet.
		if b.worker != nil && customTitle == "" && !hasFreshSummary {
			b.worker.Enqueue(summaryJob{SessionID: sessionID, CLISessionID: r.RuntimeID, SourceRef: r.SourceRef})
			enqueued++
		}
	}

	b.mu.Lock()
	b.discoveryStats = DiscoveryStats{
		LastScanAt:    t0,
		LastScanTook:  time.Since(t0),
		WindowDays:    b.discoveryWindowDays,
		TotalOnDisk:   len(records),
		NewlyImported: newly,
	}
	b.mu.Unlock()

	if newly > 0 || enqueued > 0 || time.Since(t0) > 200*time.Millisecond {
		log.Printf("[discovery] scanned %d, imported %d new, enqueued %d for summary in %v",
			len(records), newly, enqueued, time.Since(t0))
	}
}

// cleanupAdminInternalLeftovers drops the "(自动化任务,无实质内容)" placeholder
// summaries left by an earlier worker version. The current worker writes
// empty summaries instead (which the UI renders with friendly fallback
// text), so this is a one-shot migration that becomes a no-op once your
// state file is clean. Idempotent — safe on every startup.
func (b *Bridge) cleanupAdminInternalLeftovers() {
	const legacyPlaceholder = "(自动化任务,无实质内容)"
	removed := b.mgr.PurgeExternalSummaries(func(cliID string, aug session.ExternalAugmentation) bool {
		return aug.Summary == legacyPlaceholder
	})
	if removed > 0 {
		log.Printf("[bridge] cleaned up %d legacy '(自动化任务...)' placeholders from gateway_state.json — worker will skip them on next pass", removed)
		b.saveStateIfPossible()
	}
}

// DiscoveryStatsSnapshot returns a copy of the latest discovery progress
// (read by /status).
func (b *Bridge) DiscoveryStatsSnapshot() DiscoveryStats {
	b.mu.Lock()
	defer b.mu.Unlock()
	return b.discoveryStats
}

// shareExternalEnabled reads b.shareExternal under the mutex (it can change
// at runtime via /config).
func (b *Bridge) shareExternalEnabled() bool {
	b.mu.Lock()
	defer b.mu.Unlock()
	return b.shareExternal
}
